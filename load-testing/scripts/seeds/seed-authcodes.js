
/**
 * Parallel Seed Data Generation Script (Pre-created Users Version)
 *
 * V3: Pre-created users support
 * - Uses users pre-created by seed-users.js (loaded from test_users.json)
 * - Users can be reused indefinitely (unless DB is reset)
 * - Shard determined by hash of userId:clientId (sticky routing)
 *
 * Prerequisites:
 *   Create users in advance with seed-users.js
 *   Example: USER_COUNT=500000 node scripts/seed-users.js
 *
 * Usage:
 *   # Generate authorization codes (using pre-created users)
 *   AUTH_CODE_COUNT=50000 node scripts/seed-authcodes.js
 *
 * Environment variables:
 *   BASE_URL          - Target server (default: https://your-authrim.example.com)
 *   CLIENT_ID         - OAuth client ID (required)
 *   CLIENT_SECRET     - OAuth client secret (required)
 *   ADMIN_API_SECRET  - Admin API secret (required)
 *   AUTH_CODE_COUNT   - Number of authorization codes to generate (default: 1000)
 *   USER_COUNT        - Number of users to use (default: all users)
 *   CONCURRENCY       - Parallel requests (default: 10)
 *   SAVE_INTERVAL     - Auto-save interval (default: 500)
 *   OUTPUT_DIR        - Output directory (default: ./seeds)
 */

import fs from 'node:fs';
import path from 'node:path';
import crypto from 'node:crypto';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const BASE_URL = process.env.BASE_URL || '';
const CLIENT_ID = process.env.CLIENT_ID || '';
const CLIENT_SECRET = process.env.CLIENT_SECRET || '';
const REDIRECT_URI = process.env.REDIRECT_URI || 'https://localhost:3000/callback';
const ADMIN_API_SECRET = process.env.ADMIN_API_SECRET || '';
const AUTH_CODE_COUNT = Number.parseInt(process.env.AUTH_CODE_COUNT || '1000', 10);
// USER_COUNT: Number of users to use (0 = use all users)
const USER_COUNT = Number.parseInt(process.env.USER_COUNT || '0', 10);
const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const OUTPUT_DIR = process.env.OUTPUT_DIR || path.join(SCRIPT_DIR, '..', 'seeds');

// Concurrency (number of parallel requests)
const CONCURRENCY = Number.parseInt(process.env.CONCURRENCY || '10', 10);

// Auto-save interval (save to file every N codes)
const SAVE_INTERVAL = Number.parseInt(process.env.SAVE_INTERVAL || '500', 10);

if (!CLIENT_ID || !CLIENT_SECRET) {
  console.error('CLIENT_ID and CLIENT_SECRET are required.');
  process.exit(1);
}

if (!ADMIN_API_SECRET) {
  console.error('ADMIN_API_SECRET is required.');
  process.exit(1);
}

const basicAuthHeader = `Basic ${Buffer.from(`${CLIENT_ID}:${CLIENT_SECRET}`).toString('base64')}`;
const adminAuthHeader = { Authorization: `Bearer ${ADMIN_API_SECRET}` };

// Multi-user support: mapping of user IDs to sessions
const userSessions = new Map(); // userId -> sessionCookie

// Pre-created users file (generated by seed-users.js)
const TEST_USERS_FILE = path.join(OUTPUT_DIR, 'test_users.json');

/**
 * Load pre-created user list
 * @returns {{ userId: string, shardIndex: number }[]} User array
 */
function loadPreCreatedUsers() {
  if (!fs.existsSync(TEST_USERS_FILE)) {
    console.error(`‚ùå User file not found: ${TEST_USERS_FILE}`);
    console.error('   Create users first with seed-users.js:');
    console.error('   USER_COUNT=1000 node scripts/seed-users.js');
    process.exit(1);
  }

  try {
    const data = JSON.parse(fs.readFileSync(TEST_USERS_FILE, 'utf8'));
    if (Array.isArray(data)) {
      // New format: [{ index, userId, shardIndex }, ...]
      return data.map((d) => ({ userId: d.userId, shardIndex: d.shardIndex }));
    } else if (typeof data === 'object') {
      // Old format: { "key": userId, ... }
      return Object.values(data).map((userId) => ({
        userId,
        shardIndex: calculateShardIndex(userId, CLIENT_ID, 32),
      }));
    }
  } catch (err) {
    console.error(`‚ùå Failed to load user file: ${err.message}`);
    process.exit(1);
  }
  return [];
}

// Pre-created users list (lazy loaded)
let preCreatedUsers = null;

// Global variables (accessed from signal handlers)
let allCodes = [];
let newCodesCount = 0;
let isShuttingDown = false;
let currentUserIndex = 0; // For round-robin

function randomVerifier() {
  return crypto.randomBytes(48).toString('base64url');
}

function codeChallenge(verifier) {
  return crypto.createHash('sha256').update(verifier).digest('base64url');
}

/**
 * Calculate shard index using FNV-1a hash
 * Same algorithm as server-side getAuthCodeShardIndex
 */
function calculateShardIndex(userId, clientId, shardCount) {
  const input = `${userId}:${clientId}`;
  const FNV_PRIME = 0x01000193;
  const FNV_OFFSET_BASIS = 0x811c9dc5;
  let hash = FNV_OFFSET_BASIS;
  for (let i = 0; i < input.length; i++) {
    hash ^= input.charCodeAt(i);
    hash = Math.imul(hash, FNV_PRIME) >>> 0;
  }
  return hash % shardCount;
}

/**
 * Load existing codes file
 */
function loadExistingCodes() {
  const authPath = path.join(OUTPUT_DIR, 'authorization_codes.json');
  if (fs.existsSync(authPath)) {
    try {
      const content = fs.readFileSync(authPath, 'utf-8');
      const codes = JSON.parse(content);
      if (Array.isArray(codes)) {
        console.log(`  üìÇ Found existing file: ${codes.length} codes`);
        return codes;
      }
    } catch (err) {
      console.warn(`  ‚ö†Ô∏è  Could not read existing file: ${err.message}`);
    }
  }
  return [];
}

/**
 * Save codes to file
 */
function saveCodes(codes, label = '') {
  fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  const authPath = path.join(OUTPUT_DIR, 'authorization_codes.json');
  fs.writeFileSync(authPath, JSON.stringify(codes, null, 2));
  if (label) {
    console.log(`  üíæ ${label}: Saved ${codes.length} codes to ${authPath}`);
  }
}

/**
 * Graceful shutdown (Ctrl+C handling)
 */
function setupSignalHandlers() {
  const shutdown = (signal) => {
    if (isShuttingDown) return;
    isShuttingDown = true;
    console.log(`\n\n‚ö†Ô∏è  ${signal} received. Saving progress...`);
    if (allCodes.length > 0) {
      saveCodes(allCodes, 'Graceful shutdown');
    }
    console.log(`‚úÖ Saved ${allCodes.length} codes (${newCodesCount} new in this session)`);
    process.exit(0);
  };

  process.on('SIGINT', () => shutdown('SIGINT'));
  process.on('SIGTERM', () => shutdown('SIGTERM'));
}

/**
 * Create test session for a single user
 */
async function createTestSession(userId) {
  const res = await fetch(`${BASE_URL}/api/admin/test/sessions`, {
    method: 'POST',
    headers: {
      ...adminAuthHeader,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      user_id: userId,
      ttl_seconds: 28800, // 8 hours for load testing
    }),
  });

  if (!res.ok) {
    const error = await res.text();
    throw new Error(`Failed to create test session for ${userId}: ${res.status} - ${error}`);
  }

  const data = await res.json();
  return `authrim_session=${data.session_id}`;
}

/**
 * Create sessions for pre-created users in parallel
 */
async function setupPreCreatedUsers() {
  // Load pre-created users
  preCreatedUsers = loadPreCreatedUsers();
  console.log(`  üìÇ Loaded ${preCreatedUsers.length} pre-created users from ${TEST_USERS_FILE}`);

  // Determine number of users to use
  const useCount =
    USER_COUNT > 0 ? Math.min(USER_COUNT, preCreatedUsers.length) : preCreatedUsers.length;
  const usersToUse = preCreatedUsers.slice(0, useCount);
  console.log(`  üî¢ Using ${usersToUse.length} users for this run`);

  const shardCoverage = new Set();
  const batchSize = 50;
  let successCount = 0;
  let errorCount = 0;

  // Create sessions in parallel
  for (let i = 0; i < usersToUse.length; i += batchSize) {
    const batch = usersToUse.slice(i, i + batchSize).map(async (user, idx) => {
      try {
        const sessionCookie = await createTestSession(user.userId);
        const shardIndex =
          user.shardIndex >= 0 ? user.shardIndex : calculateShardIndex(user.userId, CLIENT_ID, 32);
        shardCoverage.add(shardIndex);
        userSessions.set(user.userId, { sessionCookie, shardIndex, userIndex: i + idx });
        successCount++;
        return { userId: user.userId, shardIndex };
      } catch (err) {
        errorCount++;
        if (errorCount <= 3) {
          console.error(`  ‚ö†Ô∏è  Failed to create session: ${err.message}`);
        }
        return null;
      }
    });

    await Promise.all(batch);

    if (i + batchSize < usersToUse.length) {
      process.stdout.write(`\r  [${successCount}/${usersToUse.length}] sessions created...`);
    }
  }

  console.log(`\n  ‚úÖ Created ${successCount} sessions covering ${shardCoverage.size}/32 shards`);
  if (errorCount > 0) {
    console.log(`  ‚ö†Ô∏è  ${errorCount} session creation failures`);
  }

  // Display shard distribution
  const shardDist = {};
  for (const [, { shardIndex }] of userSessions) {
    shardDist[shardIndex] = (shardDist[shardIndex] || 0) + 1;
  }
  console.log(`  üìä Shard distribution: ${JSON.stringify(shardDist)}`);

  return successCount;
}

/**
 * Get authorization code using specified user's session
 */
async function fetchAuthorizationCode(userId, sessionCookie, shardIndex) {
  const verifier = randomVerifier();
  const challenge = codeChallenge(verifier);
  const state = crypto.randomBytes(12).toString('hex');
  const nonce = crypto.randomBytes(12).toString('hex');

  const params = new URLSearchParams({
    response_type: 'code',
    client_id: CLIENT_ID,
    redirect_uri: REDIRECT_URI,
    scope: 'openid profile email',
    state,
    nonce,
    code_challenge: challenge,
    code_challenge_method: 'S256',
  });

  const res = await fetch(`${BASE_URL}/authorize?${params.toString()}`, {
    redirect: 'manual',
    headers: {
      Cookie: sessionCookie,
    },
  });

  const location = res.headers.get('location') || '';
  const body = await res.text();

  // Check for OAuth error in redirect
  if (location.includes('error=')) {
    const errorMatch = location.match(/error=([^&]+)/);
    const descMatch = location.match(/error_description=([^&]+)/);
    const error = errorMatch ? decodeURIComponent(errorMatch[1]) : 'unknown';
    const desc = descMatch ? decodeURIComponent(descMatch[1]) : '';
    throw new Error(`OAuth error: ${error} - ${desc}`);
  }

  if (res.status !== 302 && res.status !== 200) {
    throw new Error(`unexpected authorize status ${res.status}: ${body.substring(0, 200)}`);
  }

  let code = null;

  if (location.includes('code=')) {
    const match = location.match(/code=([^&]+)/);
    if (match) code = decodeURIComponent(match[1]);
  }
  if (!code && body.includes('code=')) {
    const match = body.match(/code=([^&"'>]+)/);
    if (match) code = decodeURIComponent(match[1]);
  }

  if (!code) {
    throw new Error(
      `authorization code not found in location=${location.substring(0, 100)}, status=${res.status}`
    );
  }

  return {
    code,
    code_verifier: verifier,
    redirect_uri: REDIRECT_URI,
    state,
    nonce,
    user_id: userId,
    shard_index: shardIndex,
  };
}

/**
 * Select next user using round-robin
 */
function getNextUser() {
  const userIds = Array.from(userSessions.keys());
  const userId = userIds[currentUserIndex % userIds.length];
  currentUserIndex++;
  return { userId, ...userSessions.get(userId) };
}

/**
 * Generate batch in parallel (round-robin user distribution)
 */
async function generateBatch(batchSize) {
  const promises = [];
  for (let i = 0; i < batchSize; i++) {
    const { userId, sessionCookie, shardIndex } = getNextUser();
    promises.push(
      fetchAuthorizationCode(userId, sessionCookie, shardIndex).catch((err) => {
        return { error: err.message };
      })
    );
  }
  return Promise.all(promises);
}

async function main() {
  setupSignalHandlers();

  console.log(`üöÄ Parallel seed generator (pre-created users)`);
  console.log(`  BASE_URL        : ${BASE_URL}`);
  console.log(`  AUTH_CODE_COUNT : ${AUTH_CODE_COUNT} (new codes to generate)`);
  console.log(`  USER_COUNT      : ${USER_COUNT === 0 ? 'all' : USER_COUNT} (users to use)`);
  console.log(`  CONCURRENCY     : ${CONCURRENCY}`);
  console.log(`  SAVE_INTERVAL   : ${SAVE_INTERVAL}`);
  console.log(`  OUTPUT_DIR      : ${OUTPUT_DIR}`);
  console.log('');

  // Step 0: Load existing codes
  console.log('üìÇ Step 0: Loading existing codes...');
  allCodes = loadExistingCodes();
  const existingCount = allCodes.length;
  console.log(`  Total existing: ${existingCount} codes`);
  console.log('');

  // Step 1: Load pre-created users and create sessions
  console.log('üìã Step 1: Setting up pre-created users...');
  const userCount = await setupPreCreatedUsers();
  if (userCount === 0) {
    console.error('‚ùå No users available. Please run seed-users.js first.');
    process.exit(1);
  }

  console.log('');
  console.log(`üìä Step 2: Generating ${AUTH_CODE_COUNT} new codes with shard distribution...`);

  let errorCount = 0;
  const startTime = Date.now();
  let lastSaveCount = 0;

  // Process in batches
  const totalBatches = Math.ceil(AUTH_CODE_COUNT / CONCURRENCY);

  for (let batch = 0; batch < totalBatches && !isShuttingDown; batch++) {
    const remaining = AUTH_CODE_COUNT - newCodesCount;
    const batchSize = Math.min(CONCURRENCY, remaining);

    const results = await generateBatch(batchSize);

    for (const result of results) {
      if (result.error) {
        errorCount++;
        // Log first error for debugging
        if (errorCount === 1) {
          console.log(`  ‚ö†Ô∏è  First error: ${result.error}`);
        }
      } else {
        allCodes.push(result);
        newCodesCount++;
      }
    }

    // Incremental save
    if (newCodesCount - lastSaveCount >= SAVE_INTERVAL) {
      saveCodes(allCodes, `Auto-save at ${newCodesCount} new codes`);
      lastSaveCount = newCodesCount;
    }

    const elapsed = (Date.now() - startTime) / 1000;
    const rate = newCodesCount / elapsed;

    if ((batch + 1) % 10 === 0 || batch === totalBatches - 1) {
      console.log(
        `  [${newCodesCount}/${AUTH_CODE_COUNT}] ${rate.toFixed(1)}/s, errors: ${errorCount}, total: ${allCodes.length}`
      );
    }
  }

  // Final save
  const totalTime = (Date.now() - startTime) / 1000;
  console.log('');
  console.log(`‚úÖ Generation complete:`);
  console.log(`   New codes: ${newCodesCount}`);
  console.log(`   Total codes: ${allCodes.length}`);
  console.log(`   Errors: ${errorCount}`);
  console.log(`   Time: ${totalTime.toFixed(1)}s`);
  console.log(`   Rate: ${(newCodesCount / totalTime).toFixed(1)} codes/sec`);

  if (allCodes.length === 0) {
    throw new Error('No seeds collected. Aborting.');
  }

  saveCodes(allCodes, 'Final save');
  console.log('');
  console.log(
    `üìÅ Total: ${allCodes.length} codes in ${path.join(OUTPUT_DIR, 'authorization_codes.json')}`
  );
  console.log('');
  console.log('üí° Tip: Run again to add more codes. Use AUTH_CODE_COUNT=2000 for batches.');
  console.log('üéâ done');
}

main().catch((err) => {
  console.error(err);
  // Try to save even on error
  if (allCodes.length > 0) {
    console.log('‚ö†Ô∏è  Error occurred, but saving collected codes...');
    saveCodes(allCodes, 'Error recovery save');
  }
  process.exit(1);
});
